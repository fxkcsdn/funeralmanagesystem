package com.FuneralManage.entity;

public class DeadUrn {
	
	private String urnName;
	private int urnBeCost;
	private int urnRealCost;
	public String getUrnName() {
		return urnName;
	}
	public void setUrnName(String urnName) {
		this.urnName = urnName;
	}
	public int getUrnBeCost() {
		return urnBeCost;
	}
	public void setUrnBeCost(int urnBeCost) {
		this.urnBeCost = urnBeCost;
	}
	public int getUrnRealCost() {
		return urnRealCost;
	}
	public void setUrnRealCost(int urnRealCost) {
		this.urnRealCost = urnRealCost;
	}
	
}
