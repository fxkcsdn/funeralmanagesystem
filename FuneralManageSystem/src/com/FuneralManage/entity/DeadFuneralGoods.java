package com.FuneralManage.entity;

public class DeadFuneralGoods {

	private String funeralGoodsName;
	private int funeralGoodsBeCost;
	private int funeralGoodsRealCost;
	
	public String getFuneralGoodsName() {
		return funeralGoodsName;
	}
	public void setFuneralGoodsName(String funeralGoodsName) {
		this.funeralGoodsName = funeralGoodsName;
	}
	public int getFuneralGoodsBeCost() {
		return funeralGoodsBeCost;
	}
	public void setFuneralGoodsBeCost(int funeralGoodsBeCost) {
		this.funeralGoodsBeCost = funeralGoodsBeCost;
	}
	public int getFuneralGoodsRealCost() {
		return funeralGoodsRealCost;
	}
	public void setFuneralGoodsRealCost(int funeralGoodsRealCost) {
		this.funeralGoodsRealCost = funeralGoodsRealCost;
	}
	
}
