package com.FuneralManage.entity;

public class DeadMakeBeauty {
	
	private String makeBeautyName;
	private int makeBeautyBeCost;
	private int makeBeautyRealCost;
	private String status;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMakeBeautyName() {
		return makeBeautyName;
	}
	public void setMakeBeautyName(String makeBeautyName) {
		this.makeBeautyName = makeBeautyName;
	}
	public int getMakeBeautyBeCost() {
		return makeBeautyBeCost;
	}
	public void setMakeBeautyBeCost(int makeBeautyBeCost) {
		this.makeBeautyBeCost = makeBeautyBeCost;
	}
	public int getMakeBeautyRealCost() {
		return makeBeautyRealCost;
	}
	public void setMakeBeautyRealCost(int makeBeautyRealCost) {
		this.makeBeautyRealCost = makeBeautyRealCost;
	}
}
